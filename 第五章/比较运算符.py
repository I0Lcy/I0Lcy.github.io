# == 运算符检查两个操作数是否相等，如果是，则条件成立，执行if中的代码
age = 18
if age == 18:
    print("Good")
else:
    print("No")

# != 运算符检查连个操作数的值是否不相等， 如果是， 则条件成立，返回True
weight = 20
